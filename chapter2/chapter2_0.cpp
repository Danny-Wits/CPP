// Keyword ,Identifier ,Operator,Constant, Punctuator

#include <iostream>
using namespace std;
// defining constant using pre-processors
#define dime 10

int main()
{

    int x = dime;
    /*
    Here

    int   :  keyword
    x     :  identifier
    =     :  operator
    dime  :  constant
    ;     :  punctuator

    */
    cout << x << endl;

    return 0;
}
